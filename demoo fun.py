def arithoperation(a,b):
    c=a+b
    print("add:",c)
    d=a-b
    print("sub:",d)
    e=a*b
    print("mul:",e)
    f=a/b
    print("div:",f)
    g=a%b
    print("mod:",g)
    h=a//b
    print("floor:",h)
    i=a**b
    print("exp:",i)
arithoperation(4,2)
def logical(a,b):
    c=a&b
    print("and:",c)
    d=a|b
    print("OR:",d)
    e=a^b
    print("NOT:",e)
logical(2,2)
def member(a,b):
    c=a in b
    print("in:",c)
    d=a not in b
    print("not in:",d)
member(2,4)
